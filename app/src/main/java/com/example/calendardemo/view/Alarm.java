package com.example.calendardemo.view;

public class Alarm {
    String strYear;
    String strMonth ;
    String strDay ;
    String strHour;
    String strMinute ;
    String strLecture ;
    String strLocation;
    public Alarm(String strYear,String strMonth , String strDay, String strHour, String strMinute,String strLecture, String strLocation){
        this.strYear = strYear;
        this.strMonth = strMonth;
        this.strDay = strDay;
        this.strHour = strHour;
        this.strMinute = strMinute;
        this.strLecture = strLecture;
        this.strLocation = strLocation;
    }
}
